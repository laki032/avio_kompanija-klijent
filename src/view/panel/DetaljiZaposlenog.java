    private void jbtSacuvajNovuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtSacuvajNovuActionPerformed
        String datumS = jtxtDatum.getText().trim();
        Date datum;
        try {
            datum = new SimpleDateFormat("dd/MM/yyyy").parse(datumS);
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(this, "datum mora biti u formatu dd/MM/yyyy", "GRESKA", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (tip.equals("pilot")) {
            try {
                Uloga u = new Uloga();
                Pilot p = new Pilot();
                p.setJmbg(z.getJmbg());
                u.setPilot(p);
                u.setAvion((Avion) jcomboAvionTip.getSelectedItem());
                u.setNazivUloge(jtxtUloga.getText().trim());
                u.setDatumLeta(datum);

                TransferKlasa zahtev = new TransferKlasa();
                zahtev.setOperacija(Akcija.NOVA_ULOGA);
                zahtev.setParametar(u);
                Kontroler.getINSTANCE().posalji(zahtev);
                TransferKlasa odgovor = Kontroler.getINSTANCE().primi();

                ((TableModelUloge) jtblLicenceUloge.getModel()).novaUloga(u);
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage(), "GRESKA", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            try {
                Licenca l = new Licenca();
                AvioMehanicar a = new AvioMehanicar();
                a.setJmbg(z.getJmbg());
                l.setMehanicar(a);
                l.setDatumDobijanjaLicence(datum);
                l.setTipAviona((TipAviona) jcomboAvionTip.getSelectedItem());

                TransferKlasa zahtev = new TransferKlasa();
                zahtev.setOperacija(Akcija.NOVA_LICENCA);
                zahtev.setParametar(l);
                Kontroler.getINSTANCE().posalji(zahtev);
                TransferKlasa odgovor = Kontroler.getINSTANCE().primi();

                ((TableModelLicence) jtblLicenceUloge.getModel()).novaLicenca(l);
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage(), "GRESKA", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jbtSacuvajNovuActionPerformed

