    private void jbtObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtObrisiActionPerformed
        int r = jtblLicenceUloge.getSelectedRow();
        if (r < 0) {
            JOptionPane.showMessageDialog(this, "Morate prvo odabrati zaposlenog!");
            return;
        }
        try {
            TransferKlasa zahtev = new TransferKlasa();
            if (tip.equals("pilot")) {
                Uloga u = ((TableModelUloge) jtblLicenceUloge.getModel()).vratiUlogu(r);
                zahtev.setOperacija(Akcija.BRISI_ULOGU);
                zahtev.setParametar(u);
            } else {
                Licenca l = ((TableModelLicence) jtblLicenceUloge.getModel()).vratiLicencu(r);
                zahtev.setOperacija(Akcija.BRISI_LICENCU);
                zahtev.setParametar(l);
            }
            Kontroler.getINSTANCE().posalji(zahtev);
            TransferKlasa odgovor = Kontroler.getINSTANCE().primi();
            JOptionPane.showMessageDialog(this, "Sistem je uspeo da obrise oznaceni red.", "Poruka", JOptionPane.INFORMATION_MESSAGE);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "GRESKA", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            jtblLicenceUloge.remove(r);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "GRESKA", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jbtObrisiActionPerformed

