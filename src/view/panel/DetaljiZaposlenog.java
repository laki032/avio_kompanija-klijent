    private void srediFormu() {
        try {
            jbtObrisi.setVisible(false);
            jbtSacuvajIzmenu.setVisible(false);
            TransferKlasa odgovor;
            TransferKlasa zahtev;
            tip = "pilot";
            List<Zaposleni> l;
            try {
                zahtev = new TransferKlasa();
                zahtev.setOperacija(Akcija.PRETRAZI_PILOTE_PO_KRITERIJUMU);
                zahtev.setParametar(z);
                Kontroler.getINSTANCE().posalji(zahtev);
                odgovor = Kontroler.getINSTANCE().primi();

                l = (List<Zaposleni>) odgovor.getRezultat();
                if (l.isEmpty()) {
                    throw new Exception("Nije ucitan pilot");
                }

                jlblDatumTip.setText("Datum pregleda:");
                jlblZdrStanje.setVisible(true);
                jlblProsao.setVisible(true);
            } catch (Exception e) {
                //ako se desi da nije pilot znaci da je mehanicar
                tip = "mehanicar";
                zahtev = new TransferKlasa();
                zahtev.setOperacija(Akcija.PRETRAZI_MEHANICARE_PO_KRITERIJUMU);
                zahtev.setParametar(z);
                Kontroler.getINSTANCE().posalji(zahtev);
                odgovor = Kontroler.getINSTANCE().primi();

                l = (List<Zaposleni>) odgovor.getRezultat();
                if (l.isEmpty()) {
                    JOptionPane.showMessageDialog(this, "Sistem nije uspeo da ucita zaposlenog.", "GRESKA", JOptionPane.ERROR_MESSAGE);
                    return;
                }

                jlblDatumTip.setText("Tip mehanicara:");
                jlblZdrStanje.setVisible(false);
                jlblProsao.setVisible(false);
            }

            jtxtJMBG.setEnabled(false);
            jtxtImePr.setEnabled(false);
            jtxtGodRodj.setEnabled(false);
            jtxtDatumTip.setEnabled(false);

            if (tip.equals("pilot")) {
                Pilot p = (Pilot) l.get(0);
                jtxtJMBG.setText(p.getJmbg());
                jtxtImePr.setText(p.getImePrezime());
                jtxtGodRodj.setText(p.getGodinaRodjenja() + "");
                jtxtDatumTip.setText(new SimpleDateFormat("dd/MM/yyyy").format(p.getDatumPregleda()));
                if (p.isProsaoTestZdravstvenogStanja()) {
                    jlblZdrStanje.setIcon(new ImageIcon("true.png"));
                } else {
                    jlblZdrStanje.setIcon(new ImageIcon("false.png"));
                }
            } else {
                AvioMehanicar am = (AvioMehanicar) l.get(0);
                jtxtJMBG.setText(am.getJmbg());
                jtxtImePr.setText(am.getImePrezime());
                jtxtGodRodj.setText(am.getGodinaRodjenja() + "");
                jtxtDatumTip.setText(am.getTipMehanicara());
            }
            srediTabelu(tip);
            popuniPanel(tip);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "GRESKA", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void srediTabelu(String tip) throws Exception {
        TransferKlasa zahtev = new TransferKlasa();
        if (tip.equalsIgnoreCase("pilot")) {
            zahtev.setOperacija(Akcija.PRETRAZI_ULOGE_PO_KRITERIJUMU);
            zahtev.setParametar(z);
            Kontroler.getINSTANCE().posalji(zahtev);
            TransferKlasa odgovor = Kontroler.getINSTANCE().primi();
            if (odgovor.getIzuzetak() != null) {
                throw (Exception) odgovor.getIzuzetak();
            }
            List<Uloga> l = (List<Uloga>) odgovor.getRezultat();
            jtblLicenceUloge.setModel(new TableModelUloge(l, "pregledUlogaZaPilota"));
        } else {
            zahtev.setOperacija(Akcija.PRETRAZI_LICENCE_PO_KRITERIJUMU);
            zahtev.setParametar(z);
            Kontroler.getINSTANCE().posalji(zahtev);
            TransferKlasa odgovor = Kontroler.getINSTANCE().primi();
            if (odgovor.getIzuzetak() != null) {
                throw (Exception) odgovor.getIzuzetak();
            }
            List<Licenca> l = (List<Licenca>) odgovor.getRezultat();
            jtblLicenceUloge.setModel(new TableModelLicence(l));
        }
    }

    private void popuniPanel(String tip) {
        if (tip.equals("pilot")) {
            try {
                TransferKlasa zahtev = new TransferKlasa();
                zahtev.setOperacija(Akcija.VRATI_SVE_AVIONE);
                Kontroler.getINSTANCE().posalji(zahtev);
                TransferKlasa odgovor = Kontroler.getINSTANCE().primi();
                List<Avion> listaAviona = (List<Avion>) odgovor.getRezultat();
                jcomboAvionTip.setModel(new DefaultComboBoxModel(listaAviona.toArray()));
            } catch (Exception ex) {
            }
            jlblavionTIp.setText("Avion:");
            jlblUloga.setVisible(true);
            jtxtUloga.setVisible(true);
        } else {
            try {
                TransferKlasa zahtev = new TransferKlasa();
                zahtev.setOperacija(Akcija.VRATI_SVE_TIPOVE);
                Kontroler.getINSTANCE().posalji(zahtev);
                TransferKlasa odgovor = Kontroler.getINSTANCE().primi();
                List<TipAviona> listaTipova = (List<TipAviona>) odgovor.getRezultat();
                jcomboAvionTip.setModel(new DefaultComboBoxModel(listaTipova.toArray()));
            } catch (Exception ex) {
            }
            jlblavionTIp.setText("Tip aviona:");
            jlblUloga.setVisible(false);
            jtxtUloga.setVisible(false);
        }
    }
