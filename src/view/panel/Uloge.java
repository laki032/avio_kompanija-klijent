    private void jbtObrisiUloguActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtObrisiUloguActionPerformed
        int r = jtblUloge.getSelectedRow();
        if (r == -1) {
            jlblGreska.setText("Morate izabrati ulogu!");
            return;
        }
        try {
            TransferKlasa zahtev = new TransferKlasa();
            zahtev.setOperacija(Akcija.BRISI_ULOGU);
            zahtev.setParametar(uloge.get(r));
            Kontroler.getINSTANCE().posalji(zahtev);
            TransferKlasa odgovor = Kontroler.getINSTANCE().primi();
            if (odgovor.getIzuzetak() != null) {
                throw (Exception) odgovor.getIzuzetak();
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "GRESKA", JOptionPane.ERROR_MESSAGE);
            return;
        }
        uloge.remove(r);
        jtblUloge.setModel(new TableModelUloge(uloge, sk));
    }//GEN-LAST:event_jbtObrisiUloguActionPerformed

