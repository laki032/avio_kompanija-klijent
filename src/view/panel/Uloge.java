    private void srediFormu() {
        jPanelUnosUloge.setVisible(false);
        jlblGreska.setText(" ");

        jtxtDatumNosivost.setEnabled(false);
        jtxtGodiste.setEnabled(false);
        jtxtID.setEnabled(false);
        jtxtImeNaziv.setEnabled(false);
        jtxtOcenaBrojPutnika.setEnabled(false);
        jtxtTip.setEnabled(false);

        if (sk.equals("pregledUlogaZaAvion")) {
            jlblDatumNosivost.setText("Nosivost");
            jlblGodiste.setText("Godina proizvodnje");
            jlblID.setText("Sifra");
            jlblImeNaziv.setText("Naziv");
            jlblOcenaBrojPutnika.setText("BrojPutnika");
            jlblTip.setVisible(true);
            jtxtTip.setVisible(true);

            jtxtDatumNosivost.setText(a.getNosivost() + "");
            jtxtGodiste.setText(a.getGodinaProizvodnje() + "");
            jtxtID.setText(a.getAvionID() + "");
            jtxtImeNaziv.setText(a.getOznaka() + "");
            jtxtOcenaBrojPutnika.setText(a.getBrojPutnika() + "");
            jtxtTip.setText(a.getTip() + "");

        }
        if (sk.equals("pregledUlogaZaPilota")) {
            jlblDatumNosivost.setText("Datum pregleda");
            jlblGodiste.setText("Godina rodjenja");
            jlblID.setText("JMBG");
            jlblImeNaziv.setText("Ime i prezime");
            jlblOcenaBrojPutnika.setText("Ocena zdravstvenog stanja");
            jlblTip.setVisible(false);
            jtxtTip.setVisible(false);

            jtxtDatumNosivost.setText(new SimpleDateFormat("dd.MM.yyyy.").format(p.getDatumPregleda()));
            jtxtGodiste.setText(p.getGodinaRodjenja() + "");
            jtxtID.setText(p.getJmbg() + "");
            jtxtImeNaziv.setText(p.getImePrezime() + "");
            if (p.isProsaoTestZdravstvenogStanja()) {
                jtxtOcenaBrojPutnika.setText("sposoban");
            } else {
                jtxtOcenaBrojPutnika.setText("nesposoban");
            }

        }

        srediTabelu();
    }

    private void popuniCombo() {
        try {
            if (sk.equals("pregledUlogaZaAvion")) {
                //popuni ga svim pilotima
                TransferKlasa zahtev = new TransferKlasa();
                zahtev.setOperacija(Akcija.VRATI_SVE_PILOTE);
                Kontroler.getINSTANCE().posalji(zahtev);
                TransferKlasa odgovor = Kontroler.getINSTANCE().primi();

                List<Pilot> piloti = (List<Pilot>) odgovor.getRezultat();
                jcomboAvionPilot.setModel(new DefaultComboBoxModel(piloti.toArray()));

            }
            if (sk.equals("pregledUlogaZaPilota")) {
                //popuni ga svim avionima
                TransferKlasa zahtev = new TransferKlasa();
                zahtev.setOperacija(Akcija.VRATI_SVE_AVIONE);
                Kontroler.getINSTANCE().posalji(zahtev);
                TransferKlasa odgovor = Kontroler.getINSTANCE().primi();

                List<Avion> avioni = (List<Avion>) odgovor.getRezultat();
                jcomboAvionPilot.setModel(new DefaultComboBoxModel(avioni.toArray()));

            }
        } catch (Exception ex) {
            jlblGreska.setText(ex.getMessage());
        }
    }

    private void srediTabelu() {
        //prikazuje datum u tabeli u lepom formatu
        TableCellRenderer tableCellRenderer = new DefaultTableCellRenderer() {
            SimpleDateFormat f = new SimpleDateFormat("dd/MM/yyyy");

            public Component getTableCellRendererComponent(JTable table,
                    Object value, boolean isSelected, boolean hasFocus,
                    int row, int column) {
                if (value instanceof Date) {
                    value = f.format(value);
                }
                return super.getTableCellRendererComponent(table, value, isSelected,
                        hasFocus, row, column);
            }
        };
        jtblUloge.getColumnModel().getColumn(0).setCellRenderer(tableCellRenderer);

        jtblUloge.setAutoCreateRowSorter(true);
    }
