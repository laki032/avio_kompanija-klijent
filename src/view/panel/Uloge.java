    private void jbtDodajUloguActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtDodajUloguActionPerformed
        Uloga u = new Uloga();
        jlblGreskaDatum.setText(" ");
        String sDatum = jtxtDatum.getText().trim();
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy.");
        try {
            u.setDatumLeta(sdf.parse(sDatum));
        } catch (Exception e) {
            jlblGreskaDatum.setText("Format datuma je dd/MM/yyyy.");
            return;
        }
        u.setNazivUloge(jtxtNazivUloge.getText().trim());
        if (sk.equals("pregledUlogaZaAvion")) {
            //setuj mu avion sa leve i izabranog pilota
            u.setAvion(a);
            u.setPilot((Pilot) jcomboAvionPilot.getSelectedItem());
        }
        if (sk.equals("pregledUlogaZaPilota")) {
            //setuj mu pilota sa leve i izabrani avion
            u.setPilot(p);
            u.setAvion((Avion) jcomboAvionPilot.getSelectedItem());
        }
        try {
            TransferKlasa zahtev = new TransferKlasa();
            zahtev.setOperacija(Akcija.NOVA_ULOGA);
            zahtev.setParametar(u);
            Kontroler.getINSTANCE().posalji(zahtev);
            TransferKlasa odgovor = Kontroler.getINSTANCE().primi();
            if (odgovor.getIzuzetak() != null) {
                throw (Exception) odgovor.getIzuzetak();
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "GRESKA", JOptionPane.ERROR_MESSAGE);
            return;
        }
        uloge.add(u);
        jtblUloge.setModel(new TableModelUloge(uloge, sk));
        srediTabelu();
    }//GEN-LAST:event_jbtDodajUloguActionPerformed


